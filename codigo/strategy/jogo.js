class Jogo {
    constructor(jogo, preco) {
        this.jogo = jogo;
        this.preco = preco;
    }

    getJogo() {
        return this.jogo;
    }

    setJogo(jogo) {
        this.jogo = jogo;
    }

    getPreco() {
        return this.preco;
    }

    setPreco(preco) {
        this.preco = preco;
    }
}